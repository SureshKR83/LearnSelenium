package Wk1.Day2;

public class LearnDataTypes1 {
	
	public static void main(String[] args)
	{
		float cityTemp = 98.3f;
		String devType = "Microsoft Office";
		boolean isBeta = true;
		int releaseYear = 2024;
		char shortcutKey = 's';
		
		System.out.println("Learn Data Type");
		System.out.println("\n");
		
		System.out.println("Float Value " +cityTemp);
		System.out.println("String Value " +devType);
		System.out.println("Boolean Value " +isBeta);
		System.out.println("releaseYear Value " +releaseYear);
		System.out.println("Character Value " +shortcutKey);

	}

}
